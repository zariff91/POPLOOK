package com.tiseno.poplook.functions;

public class MotherAndDaughterItem {


    String motherAndDaughterID,motherAndDaughterName;

    public MotherAndDaughterItem(String motherAndDaughterID, String motherAndDaughterName) {
        super();
        this.motherAndDaughterID = motherAndDaughterID;
        this.motherAndDaughterName = motherAndDaughterName;

    }

    public String motherAndDaughterID() {
        return motherAndDaughterID;
    }

    public String motherAndDaughterName() {
        return motherAndDaughterName;
    }
}
